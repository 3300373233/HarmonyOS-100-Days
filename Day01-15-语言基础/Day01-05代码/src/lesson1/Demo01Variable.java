public class Demo01Variable
{
	public static void main(String[] args){
		//定义一个int类型的变量，变量名为age，赋值为13
		int age = 13;
		//打印输出这个变量
		System.out.println(age);
		//更改变量的值
		age = 14;//就是重新赋值，会覆盖原来的数值
		System.out.println(age);
		//定义一个字符串类型的变量，用于存储名字
		String name = "王二狗";
		//打印这个变量
		System.out.println(name);
	}
}